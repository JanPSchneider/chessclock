.include "xc.inc"

; *****************************************************************************
; *****************************************************************************
; Section: File Scope or Global Constants
; *****************************************************************************
; *****************************************************************************

    .global     _EnSecOsc

    .section .text

/******************************************************************************
  Enable Secondary Osc
*******************************************************************************/
_EnSecOsc:

        ;OSCCONL(Low byte) Unlock Sequence
        mov     #OSCCONL, w1
        mov.b     #0x02, w0
        mov     #0x46, w2
        mov     #0x57, w3
        mov.b     w2, [w1]
        mov.b     w3, [w1]

        ; Enable Sec Osc
        mov.b     w0, [w1]
    

           return

.end
